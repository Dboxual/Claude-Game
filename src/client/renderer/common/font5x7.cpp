std::vector<std::uint8_t> buildAtlas() {
    std::vector<std::uint8_t> pixels(size_t(kAtlasW) * kAtlasH, 0);
    for (const Glyph& g : kGlyphs) {
        int index = g.c - kFirstChar;
        if (index < 0 || index >= kCols * kRows) continue;
        int cellX = (index % kCols) * kCellW;
        int cellY = (index / kCols) * kCellH;
        for (int row = 0; row < kGlyphH; ++row) {
            const char* line = g.rows[row];
            for (int col = 0; col < kGlyphW && line[col] != '\0'; ++col) {
                if (line[col] == '#') {
                    pixels[size_t(cellY + row) * kAtlasW + cellX + col] = 255;
                }
            }
        }
    }
    return pixels;
}

int cellIndex(char c) {
    char up = char(std::toupper(static_cast<unsigned char>(c)));
    int index = up - kFirstChar;
    if (up == ' ' || index < 0 || index >= kCols * kRows) return -1;
    return index;
}

float textWidth(const std::string& text, float scale) {
    if (text.empty()) return 0.0f;
    return float(text.size()) * kCellW * scale - scale; // minus trailing gutter
}

